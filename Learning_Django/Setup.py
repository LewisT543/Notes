                        #### DJANGO SETUP ####

# 1. Virtual environment setup (see virtualenv Notes for help)
# 2. cd to destination repository
# 3. Using pip, install django (keep note of every import/requirement)
# 4. run Django-admin startproject <name_of_project>
    # should create a bunch of files
# 5. cd into django_project folder
# 6. run > Python manage.py runserver to launch server
# 7. create a django-app, run > Python manage.py startapp <name_of_app>
    # should create another bunch of files.
# 8. Update settings.py file, adding our new app to INSTALLED_APPS.
# 9. Create a templates folder in the app directory (path: base/templates/base)
